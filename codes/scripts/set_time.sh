#!/bin/bash

# Usage: set_time

#-----------------------------------------------------------------------------------
# Sets the time of the host computer using the local
# computer time.
#
# Remember to give execute permission to the script by:
# $ chmod +x /path/to/script.sh
#-----------------------------------------------------------------------------------

# ==================================================================================
# define parameters
# ==================================================================================

# host name
host=odroid

# host ip address
ip=10.42.0.104

# ==================================================================================
# set time
# ==================================================================================
sudo ssh -t $host@$ip "sudo date --set=\"$(date)\""
